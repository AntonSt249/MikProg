/*
 * ISR for externel interrupts triggered by the buttons
 */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	// Button left: show temperature
	if (GPIO_Pin == GPIO_PIN_0)
	{
		// Stop timer 7 to disable automatically switching between temperature and humidity
		HAL_TIM_Base_Stop_IT(&htim7);
		isHum = 0;
	}
	// Button right: show humidity
	else if (GPIO_Pin == GPIO_PIN_1)
	{
		HAL_TIM_Base_Stop_IT(&htim7);
		isHum = 1;
	}
	// Button center: Enable automatically switching between temperature and humidity
	else if (GPIO_Pin == GPIO_PIN_3)
	{
		// Resetting the timer's counter register so it start always with 5s left
		__HAL_TIM_SET_COUNTER(&htim7, 0);
		// Starting the timer 7 which switches every 5s
		HAL_TIM_Base_Start_IT(&htim7);
		// Toggle flag and reload output to switch display directly after pressing the button
		isHum ^= 1;
		reloadNumber(&data[isHum * 4]);
	}
}

/*
 * Timer ISR
 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	// If triggered by timer 6 (ca. 300Hz): Send the values for the current digit per SPI to the shift registers; Switch digit
	if (htim == &htim6)
	{
		// Reload the output field with the right data (temperature: data[0]; humidity: data[4])
		reloadNumber(&data[isHum * 4]);
		HAL_SPI_Transmit_IT(&hspi1, &number[currentDigit], 1);
		currentDigit = (currentDigit + 1) % 4;
	}
	// If triggered by timer 7 (every 5s if enabled): Toggle between temperature and humidity and reload output
	else if (htim == &htim7)
	{
		isHum ^= 1;
		reloadNumber(&data[(isHum) * 4]);
	}
}

/*
 * ISR called when SPI transmission completed
 */
void HAL_SPI_TxCpltCallback(SPI_HandleTypeDef *hspi)
{
	// Generate a rising edge to the shift registers' latch pin to output the transmitted values
	HAL_GPIO_WritePin(GPIOF, GPIO_PIN_14, RESET);
	HAL_GPIO_WritePin(GPIOF, GPIO_PIN_14, SET);
}

