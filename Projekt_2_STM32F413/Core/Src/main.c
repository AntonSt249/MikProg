/*
 * Sets the bitmaps in the number field given the data string in the format [t-32h 67]
 */
void reloadNumber(char *display)
{
	// Checking if t or h should be displayed as first character
	if (display[0] == 't')
	{
		number[0] = switch_digits[0] << 8 | digits[12] << 1 | 1;	//
	}
	else
	{
		number[0] = switch_digits[0] << 8 | digits[13] << 1 | 1;
	}

	// Checking if the value is negative (show '-'), positive or has three digits for the second digit
	if (display[1] == ' ')
	{
		number[1] = switch_digits[1] << 8 | digits[10] << 1 | 1;

	}
	else if (display[1] == '-')
	{
		number[1] = switch_digits[1] << 8 | digits[11] << 1 | 1;
	}
	else
	{
		number[1] = switch_digits[1] << 8 | digits[display[1] - 0x30] << 1 | 1;
	}

	// Setting the last two digits
	number[2] =  switch_digits[2] << 8 | digits[display[2] - 0x30] << 1 | 1;
	number[3] =  switch_digits[3] << 8 | digits[display[3] - 0x30] << 1 | 1;
}
